const mongoose = require("mongoose");

const portfolioFormSchema = mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      trim: true,
    },
    subject: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const PortfolioForm = mongoose.model("PortfolioForm", portfolioFormSchema);

module.exports = PortfolioForm;
